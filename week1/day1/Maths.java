package week1.day1;

public class Maths {
	
	public int Add(int a , int b)
	{
		int c;
		c=a+b;
		return c ;
	}
	
	public int Sub(int a , int b)
	{
		int c;
		c=a-b;
		return c;
	}
	public int Mul(int a , int b)
	{ 
		int c;
		c=a*b;
		return c;
	}
	public int Div(int a , int b)
	{
		int c;
		c=a/b;
		return c;
	}
}
